export function buildAboutSchema(baseUrl: string) {
  const normalizedBase = baseUrl.replace(/\/$/, "")

  const aboutPage = {
    "@context": "https://schema.org",
    "@type": "AboutPage",
    name: "About PortraitWiz",
    url: `${normalizedBase}/about`,
    description:
      "PortraitWiz is an AI-powered professional headshot generator trusted by 50,000+ professionals worldwide.",
    isPartOf: {
      "@type": "WebSite",
      name: "PortraitWiz",
      url: normalizedBase,
    },
  }

  const organization = {
    "@context": "https://schema.org",
    "@type": "Organization",
    name: "PortraitWiz",
    url: normalizedBase,
    description: "AI-powered professional headshot generator trusted by 50,000+ professionals.",
    foundingDate: "2022",
    logo: `${normalizedBase}/logo.png`,
    sameAs: [
      "https://twitter.com/portraitwiz",
      "https://www.linkedin.com/company/portraitwiz",
    ],
    aggregateRating: {
      "@type": "AggregateRating",
      ratingValue: "4.9",
      ratingCount: "2847",
      bestRating: "5",
    },
    contactPoint: [
      {
        "@type": "ContactPoint",
        contactType: "customer support",
        email: "support@portraitwiz.com",
        areaServed: "Worldwide",
        availableLanguage: ["English"],
      },
    ],
  }

  const breadcrumb = {
    "@context": "https://schema.org",
    "@type": "BreadcrumbList",
    itemListElement: [
      {
        "@type": "ListItem",
        position: 1,
        name: "Home",
        item: normalizedBase,
      },
      {
        "@type": "ListItem",
        position: 2,
        name: "About PortraitWiz",
        item: `${normalizedBase}/about`,
      },
    ],
  }

  const faq = {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: aboutFaq.items.map((item) => ({
      "@type": "Question",
      name: item.question,
      acceptedAnswer: {
        "@type": "Answer",
        text: item.answer,
      },
    })),
  }

  return JSON.stringify([aboutPage, organization, breadcrumb, faq])
}
